    private void cargar_tabla() {
        lst_rutina = crud.ListarRutinas();
        if (lst_rutina.size() != -1) {
            for (Rutina rutina : lst_rutina) {
                dtm.addRow(new Object[]{rutina.getNombre(), rutina.getDescripcion()});
            }
        }

    }
