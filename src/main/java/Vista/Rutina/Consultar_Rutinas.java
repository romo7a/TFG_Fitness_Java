    private void tb_rutinaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_rutinaMouseClicked
        // TODO add your handling code here:
        dlm_ejercicios.clear();
        int index = tb_rutina.getSelectedRow();
        if (index != -1) {
            for (Ejercicio ejercicio : lst_rutina.get(index).getEjercicioList()) {
                dlm_ejercicios.addElement((Ejercicio) ejercicio);
            }
        }
    }//GEN-LAST:event_tb_rutinaMouseClicked

    private void lst_ejercicioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_ejercicioMouseClicked
        // TODO add your handling code here:
        int index = lst_ejercicio.getSelectedIndex();
        if (index != -1) {
            Ejercicio e = (Ejercicio) dlm_ejercicios.get(lst_ejercicio.getSelectedIndex());
            if (e.getImagen() != null) {
                lbl_imagen.setIcon(new ImageIcon(new ImageIcon(e.getImagen()).getImage().getScaledInstance(lbl_imagen.getWidth(), lbl_imagen.getHeight(), Image.SCALE_DEFAULT)));
            } else {
                lbl_imagen.setIcon(new ImageIcon());
            }

        }

    }//GEN-LAST:event_lst_ejercicioMouseClicked

